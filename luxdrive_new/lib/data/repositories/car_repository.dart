        name: raw['name'] as String,
        brand: raw['brand'] as String,
        price: (raw['price'] as num).toDouble(),
        imagePath: 'assets/images/cars/car_$imageIndex.jpg',
        galleryImages: [
          'assets/images/cars/car_$imageIndex.jpg',
          'assets/images/cars/car_${(imageIndex % 150) + 1}.jpg',
          'assets/images/cars/car_${((imageIndex + 1) % 150) + 1}.jpg',
        ],
        description: raw['description'] as String,
        year: raw['year'] as String,
        category: raw['category'] as String,
        isNew: raw['isNew'] as bool,
        isFeatured: raw['isFeatured'] as bool,
        rating: 4.0 + (idx % 10) * 0.1,
        reviewCount: 80 + (idx * 17) % 200,
        colors: (raw['colors'] as List).cast<String>(),
        specs: CarSpecs(
          engine: raw['engine'] as String,
          horsepower: raw['horsepower'] as String,
          torque: raw['torque'] as String,
          acceleration: raw['acceleration'] as String,
          topSpeed: raw['topSpeed'] as String,
          transmission: raw['transmission'] as String,
          drivetrain: raw['drivetrain'] as String,
          fuelType: raw['fuelType'] as String,
          fuelConsumption: raw['fuelConsumption'] as String? ?? 'N/A',
          seating: raw['seating'] as String,
          weight: raw['weight'] as String,
          range: raw['range'] as String? ?? 'N/A',
        ),
      );
    }).toList();
  }

  List<CarModel> getFeaturedCars() =>
      getAllCars().where((c) => c.isFeatured).toList();

  List<CarModel> getNewArrivals() =>
      getAllCars().where((c) => c.isNew).toList();

  List<String> getAllBrands() =>
      getAllCars().map((c) => c.brand).toSet().toList()..sort();

  List<String> getAllCategories() =>
      getAllCars().map((c) => c.category).toSet().toList()..sort();
